
package org.rasterdb.service.wrap;

/**
 *
 * @author Administrator
 */
public class XzqhResultWrap<T,V> {
     T data;
     V geo;

    public XzqhResultWrap(T data, V geo) {
        this.data = data;
        this.geo = geo;
    }

     
    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public V getGeo() {
        return geo;
    }

    public void setGeo(V geo) {
        this.geo = geo;
    }
}
