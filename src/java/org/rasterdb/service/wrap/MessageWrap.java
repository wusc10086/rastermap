package org.rasterdb.service.wrap;

/**
 * 返回查询结果包装类、泛型类
 * @author gis
 * @param <T>
 */
public class MessageWrap<T>{
    
   public int code;
   public T message;

    public MessageWrap(int code, T message) {
        this.code = code;
        this.message = message;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public T getMessage() {
        return message;
    }

    public void setMessage(T message) {
        this.message = message;
    }
}
